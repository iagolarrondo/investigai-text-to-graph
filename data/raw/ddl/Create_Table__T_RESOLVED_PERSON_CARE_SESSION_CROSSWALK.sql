/*
 * Each entry in T_RESOLVED_PERSON_CARE_SESSION_CROSSWALK corresponds to a person (RES_PERSON_ID) providing care on a care session (NORM_CARE_SESSION_ID). 
 * The only possible value for EDGE_NAME is 'PROVIDED_CARE_ON_SESSION'.
 */
CREATE TABLE {catalog_name}.{schema_name}.T_RESOLVED_PERSON_CARE_SESSION_CROSSWALK
(
	RES_PERSON_ID BIGINT, /* FK to T_RESOLVED_PERSON */
	EDGE_NAME VARCHAR(24) NOT NULL,
	EDGE_DETAIL INT,
	EDGE_DETAIL_DSC INT,
	NORM_CARE_SESSION_ID INT /* FK to T_NORM_CARE_SESSION */
)